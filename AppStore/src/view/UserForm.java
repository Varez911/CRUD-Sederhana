    private int trackHarga(){
        String query = "select hargakamar from kamar inner join jeniskamar on jeniskamar.idjeniskamar = kamar.idjeniskamar where idkamar='" + jFormattedTextField_IdKamar.getText() + "'";
        ConnectionManager conMan = new ConnectionManager();
        Connection conn = (Connection) conMan.logOn();
        int harga = 0;
        try{
            Statement stm = conn.createStatement(); //Missing Library
            ResultSet rs = stm.executeQuery(query); //Missing Library
            
            while(rs.next()){
                harga = rs.getInt("hargakamar");
            }
        }
        catch (SQLException ed){
//            Logger log = new Logger();
            System.out.println(ed);
        }
        return harga;
    }
    
