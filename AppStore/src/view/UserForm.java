    private void jButton_SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SimpanActionPerformed
          java.util.Date time1 = jDateChooser_tglTransaksi.getDate();
          java.util.Date time2 = jDateChooser_CheckIn.getDate();
          java.util.Date time3 = jDateChooser_CheckOut.getDate();
          java.sql.Date Transaksi = new java.sql.Date(time1.getTime());
          java.sql.Date CheckIn = new java.sql.Date(time2.getTime());
          java.sql.Date CheckOut = new java.sql.Date(time3.getTime());
//         System.out.println(Transaksi + " " + CheckIn); 
        savePelanggan();
//        System.out.println(trackPelanggan());
        long result = time3.getTime() - time2.getTime();
        result =  result / (24 * 60 * 60 * 1000);
        int hari = (int)result;
        Transaksi trans = new Transaksi();
        trans.setIdpelanggan(trackPelanggan());
        trans.setIdkamar(Integer.parseInt(jFormattedTextField_IdKamar.getText().replaceAll(",", "")));
        trans.setTgltransaksi(Transaksi);
        trans.setTglCheckIn(CheckIn);
        trans.setTglCheckOut(CheckOut);
        trans.setTotalharga(trackHarga() * hari);
        ExecuteTransaksi newTransaksi = new ExecuteTransaksi();
        int Hasil = newTransaksi.insertTransaksi(trans);
        System.out.println(Hasil );
    }//GEN-LAST:event_jButton_SimpanActionPerformed

